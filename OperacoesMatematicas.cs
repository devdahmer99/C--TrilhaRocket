namespace Estudos;
public class OperacoesMatematicas
{
    public int Adicionar(int valor1, int valor2)
    {
        var resultado = valor1 + valor2;
        return resultado;
    }

    public int Subtrair(int valor1, int valor2)
    {
        var resultado = valor1 - valor2;

        return resultado;
    }
}
